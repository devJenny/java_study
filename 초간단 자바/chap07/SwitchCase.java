package chap07;

public class SwitchCase {
    public static void main(String[] args) {
        int month = 3;
        switch (month) {
            case 1:
                System.out.println("1월");
                break;
            case 2:
                System.out.println("2월");
                break;
            case 3:
                System.out.println("3월");
                break;
            case 4:
                System.out.println("4월");
                break;
            default:
                System.out.println("일치하는 값 없음");
        }
    }
}
